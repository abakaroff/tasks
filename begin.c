/*
 * Задание: Дано число A. Вычислить A⁸, используя вспомогательную
 * переменную и три операции умножения. Для этого последовательно
 * находить A², A⁴, A⁸. Вывести все найденные степени числа A.
 * Дата: 03.05.25
 * Контрольный пример:
 *   Вход: 2.0
 *   Выход: 
 *     A² = 4.00
 *     A⁴ = 16.00
 *     A⁸ = 256.00
 * Время выполнения: 5 минут
 */

 #include <stdio.h>

 int main() {
     double a;
     scanf("%lf", &a);
 
     double a2 = a * a;  // A²
     double a4 = a2 * a2;  // A⁴ = (A²)²
     double a8 = a4 * a4;  // A⁸ = (A⁴)²
 
     printf("A² = %.2f\n", a2);
     printf("A⁴ = %.2f\n", a4);
     printf("A⁸ = %.2f\n", a8);
 
     return 0;
 }