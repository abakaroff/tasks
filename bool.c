/*
 * Задание: Даны координаты поля шахматной доски x, y (целые числа 1-8).
 * Учитывая, что поле (1,1) - черное, проверить истинность высказывания:
 * "Данное поле является белым".
 * Дата: 03.05.25
 * Контрольный пример:
 *   (1,1) -> черное
 *   (1,2) -> белое
 * Время выполнения: 12 минут
 */

 #include <stdio.h>
 #include <stdbool.h>
 
 bool isWhiteSquare(int x, int y) {
     // Поле белое, если сумма координат нечетная
     return (x + y) % 2 != 0;
 }
 
 int main() {
     printf("(1, 1) -> %s\n", isWhiteSquare(1, 1) ? "белое" : "черное");
     printf("(1, 2) -> %s\n", isWhiteSquare(1, 2) ? "белое" : "черное");
     printf("(5, 5) -> %s\n", isWhiteSquare(5, 5) ? "белое" : "черное");
     printf("(8, 8) -> %s\n", isWhiteSquare(8, 8) ? "белое" : "черное");
 
     return 0;
 }