/*
 * Задание: Дано целое число. Если оно положительное - увеличить на 1,
 * иначе оставить без изменений. Вывести результат.
 * Дата: 03.05.25
 * Контрольный пример:
 *   Вход: 5 → 6
 *   Вход: -3 → -3
 *   Вход: 0 → 0
 * Время выполнения: 0 минут
 */

 #include <stdio.h>

 int main() {
     int number;
     
     scanf("%d", &number);
     
     if (number > 0) {
         number += 1;
     }
     
     printf("%d\n", number);
     
     printf("\nТестовые примеры:\n");
     printf("5 → %d\n", (5 > 0) ? 5 + 1 : 5);
     printf("-3 → %d\n", (-3 > 0) ? -3 + 1 : -3);
     printf("0 → %d\n", (0 > 0) ? 0 + 1 : 0);
     
     return 0;
 }